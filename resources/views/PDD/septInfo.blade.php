                <p>Отражена норма о запрете эксплуатации транспортного средства с неисправной антиблокировочной системой тормозов (АБС) в случае, если она предусмотрена заводской комплектацией автомобиля. Такая неисправность выявляется с помощью светового индикатора мониторинга
                    рабочего состояния антиблокировочной тормозной системы. Необходимо отметить, что данная
                    неисправность и ранее была обязательной к устранению при допуске транспортного средства к
                    участию в дорожном движении согласно Технического регламента.</p>
                <b>ДВИГАТЕЛЬ</b>
                <p>Более подробно сформулированы виды неисправностей систем питания и выпуска отработавших газов транспортного средства. Конкретизированы требования к системе питания двигателей, работающих на газобаллонном оборудовании (ГБО). Например, на каждый газовый баллон должен иметься паспорт, оформленный его изготовителем (выдается при продаже вместе с
                    баллоном). При этом не допускается утечка газа, использование газовых баллонов с истекшим
                    сроком их периодического освидетельствования и нарушение креплений системы ГБО.</p>
                <b>ОБЗОРНОСТЬ</b>
                <p>В части детализации видов нарушений обзорности транспортного средства в новом
                    Перечне по сравнению с ранее применявшимся изменений не произошло. Положение, согласно
                    которому эксплуатация автомобиля запрещается в случае, если на его лобовое стекло установлены предметы, ограничивающие обзорность, кроме зеркал заднего вида, деталей стеклоочистителей, наружных и нанесенных или встроенных в стекла радиоантенн, нагревательных элементов
                    устройств размораживания и осушения ветрового стекла применялось и прежде. Запрета на использование видеорегистратора, если указанное техническое средство не мешает обзорности, оно
                    в себя не включает.</p>
                <p>Кроме того, вступившим в силу Перечнем неисправностей установлены дополнительные
