                <p>При этом ряд ранее существовавших требований, которые непосредственно не влияют на
                    вероятность совершения ДТП, теперь исключены из Перечня. Так, с 1 сентября отсутствие медицинской аптечки, огнетушителя и знака аварийной остановки не является основанием для запрещения эксплуатации транспортного средства и привлечения водителя к административной ответственности.</p>
