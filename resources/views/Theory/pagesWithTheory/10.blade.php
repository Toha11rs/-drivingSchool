    <b>10.1.</b>
    Водитель должен вести транспортное средство со скоростью, не превышающей установленного ограничения, учитывая при этом интенсивность движения, особенности и состояние транспортного средства и груза, дорожные и метеорологические условия, в частности видимость в направлении движения. Скорость должна обеспечивать водителю возможность постоянного контроля за движением транспортного средства для выполнения требований Правил.
    <p>
        При возникновении опасности для движения, которую водитель в состоянии обнаружить, он должен принять возможные меры к снижению скорости вплоть до остановки транспортного средства.
    </p>
</div>
<a name="10.2"></a>
<div class="znakrow">
    <hr/>
    <b>10.2.</b>
    В населенных пунктах разрешается движение транспортных средств со скоростью не более 60 км/ч, а в жилых зонах, велосипедных зонах и на дворовых территориях не более 20 км/ч.
    <p>
        Примечание. По решению органов исполнительной власти субъектов Российской Федерации может разрешаться повышение скорости (с установкой соответствующих знаков) на участках дорог или полосах движения для отдельных видов транспортных средств, если дорожные условия обеспечивают безопасное движение с большей скоростью. В этом случае величина разрешенной скорости не должна превышать значения, установленные для соответствующих видов транспортных средств на автомагистралях.
    </p>
</div>
<a name="10.3"></a>
<div class="znakrow">
    <hr/>
    <b>10.3.</b>
    Вне населенных пунктов разрешается движение:
    <ul>
        <li>
            <p>
                мотоциклам, легковым автомобилям и грузовым автомобилям с разрешенной максимальной массой не более 3,5 т на автомагистралях - со скоростью не более 110 км/ч, на остальных дорогах - не более 90 км/ч;
            </p>
        </li>
        <li>
            <p>
                автобусам, в которых места для сидения пассажиров оборудованы ремнями безопасности, предназначенным для перевозки исключительно сидящих пассажиров, - не более 90 км/ч, другим автобусам - не более 70 км/ч;
            </p>
        </li>
        <li>
            <p>
                легковым автомобилям при буксировке прицепа, грузовым автомобилям с разрешенной максимальной массой более 3,5 т на автомагистралях - не более 90 км/ч, на остальных дорогах - не более 70 км/ч;
            </p>
        </li>
        <li>
            <p>
                грузовым автомобилям, перевозящим людей в кузове, - не более 60 км/ч;
            </p>
        </li>
        <li>
            <p>
                автобусам, осуществляющим организованные перевозки групп детей, - не более 60 км/ч;
            </p>
        </li>
        <li>
            <p>
                Примечание. По решению собственников или владельцев автомобильных дорог может разрешаться повышение скорости на участках дорог для отдельных видов транспортных средств, если дорожные условия обеспечивают безопасное движение с большей скоростью. В этом случае величина разрешенной скорости не должна превышать значения 130 км/ч на дорогах, обозначенных знаком <a href="znak5#5.1">5.1</a>&nbsp;<a href="znak5#5.1"><img class="textimg" src="/pdd/img/z5.1.png"/></a>, и 110 км/ч на дорогах, обозначенных знаком <a href="znak5#5.3">5.3</a>&nbsp;<a href="znak5#5.3"><img class="textimg" src="/pdd/img/z5.3.png"/></a>.
            </p>
        </li>
    </ul>
</div>
<a name="10.4"></a>
<div class="znakrow">
    <hr/>
    <b>10.4.</b>
    Транспортным средствам, буксирующим механические транспортные средства, разрешается движение со скоростью не более 50 км/ч.
    <p>
        Тяжеловесным транспортным средствам, крупногабаритным транспортным средствам и транспортным средствам, осуществляющим перевозки опасных грузов, разрешается движение со скоростью, не превышающей скорости, указанной в специальном разрешении, при наличии которого в соответствии с законодательством об автомобильных дорогах и о дорожной деятельности допускается движение по автомобильным дорогам таких транспортных средств.
    </p>
</div>
<a name="10.5"></a>
<div class="znakrow">
    <hr/>
    <b>10.5.</b>
    Водителю запрещается:
    <ul>
        <li>
            <p>
                превышать максимальную скорость, определенную технической характеристикой транспортного средства;
            </p>
        </li>
        <li>
            <p>
                превышать скорость, указанную на опознавательном знаке "Ограничение скорости", установленном на транспортном средстве;
            </p>
        </li>
        <li>
            <p>
                создавать помехи другим транспортным средствам, двигаясь без необходимости со слишком малой скоростью;
            </p>
        </li>
        <li>
            <p>
                резко тормозить, если это не требуется для предотвращения дорожно-транспортного происшествия.
            </p>
        </li>
    </ul>
</div>
