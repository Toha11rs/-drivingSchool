<a name="20.1"></a>
<div class="znakrow">
    <hr/>
    <b>20.1.</b>
    Буксировка на жесткой или гибкой сцепке должна осуществляться только при наличии водителя за рулем буксируемого транспортного средства, кроме случаев, когда конструкция жесткой сцепки обеспечивает при прямолинейном движении следование буксируемого транспортного средства по траектории буксирующего.
</div>
<a name="20.2"></a>
<div class="znakrow">
    <hr/>
    <b>20.2.</b>
    При буксировке на гибкой или жесткой сцепке запрещается перевозка людей в буксируемом автобусе, троллейбусе и в кузове буксируемого грузового автомобиля, а при буксировке путем частичной погрузки - нахождение людей в кабине или кузове буксируемого транспортного средства, а также в кузове буксирующего.
</div>
<a name="20.2(1)"></a>
<div class="znakrow">
    <hr/>
    <b>20.2(1).</b>
    При буксировке управление буксирующими транспортными средствами должно осуществляться водителями, имеющими право на управление транспортными средствами в течение 2 и более лет.
</div>
<a name="20.3"></a>
<div class="znakrow">
    <hr/>
    <b>20.3.</b>
    При буксировке на гибкой сцепке должно быть обеспечено расстояние между буксирующим и буксируемым транспортными средствами в пределах 4 - 6 м, а при буксировке на жесткой сцепке - не более 4 м.
    <p>
        Гибкое связующее звено должно быть обозначено в соответствии с <a href="dopusk#9">пунктом 9 Основных положений</a>.
    </p>
</div>
<a name="20.4"></a>
<div class="znakrow">
    <hr/>
    <b>20.4.</b>
    Буксировка запрещается:
    <ul>
        <li>
            <p>
                транспортных средств, у которых не действует рулевое управление <b>**</b> (допускается буксировка методом частичной погрузки);
            </p>
        </li>
        <li>
            <p>
                двух и более транспортных средств;
            </p>
        </li>
        <li>
            <p>
                транспортных средств с недействующей тормозной системой <b>**</b>, если их фактическая масса более половины фактической массы буксирующего транспортного средства. При меньшей фактической массе буксировка таких транспортных средств допускается только на жесткой сцепке или методом частичной погрузки;
            </p>
        </li>
        <li>
            <p>
                двухколесными мотоциклами без бокового прицепа, а также таких мотоциклов;
            </p>
        </li>
        <li>
            <p>
                в гололедицу на гибкой сцепке.
            </p>
        </li>
    </ul>
    <p><b>**</b> Недействующими считаются системы, которые не позволяют водителю остановить транспортное средство или осуществить маневр при движении даже с минимальной скоростью.</p>
</div>
