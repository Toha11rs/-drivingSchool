    <b>16.2.</b>
    При вынужденной остановке на проезжей части водитель должен обозначить транспортное средство в соответствии с требованиями раздела <a href="pdd7">7</a> Правил и принять меры для того, чтобы вывести его на предназначенную для этого полосу (правее линии, обозначающей край проезжей части).
</div>
