    <b>16.1.</b>
    На автомагистралях запрещается:
    <ul>
        <li>
            <p>
                движение пешеходов, лиц, использующих для передвижения средства индивидуальной мобильности, домашних животных, велосипедов, мопедов, тракторов и самоходных машин, иных транспортных средств, скорость которых по технической характеристике или их состоянию менее 40 км/ч;
            </p>
        </li>
        <li>
            <p>
                движение грузовых автомобилей с разрешенной максимальной массой более 3,5 т далее второй полосы;
            </p>
        </li>
        <li>
            <p>
                остановка вне специальных площадок для стоянки, обозначенных знаком <a href="znak6#6.4">6.4</a>&nbsp;<a href="znak6#6.4"><img class="textimg" src="/pdd/img/z6.4.png"/></a> или <a href="znak7#7.11">7.11</a>&nbsp;<a href="znak7#7.11"><img class="textimg" src="/pdd/img/z7.11.png"/></a>;
            </p>
        </li>
        <li>
            <p>
                разворот и въезд в технологические разрывы разделительной полосы;
            </p>
        </li>
        <li>
            <p>
                движение задним ходом;
            </p>
        </li>
</div>
